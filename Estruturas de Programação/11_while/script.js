let x = 10
while(x > 0){
    console.log('o X é igual a ' + x); //enquanto o x for maior que 0, irá imprimir a mensagem no console.
    x = x - 1;// cada vez que o loop for feito o valor de x (10) subtrairá menos 1.
}

let y = 0
while(y <= 10){
    console.log(y) // enquanto y for menor ou igual a 10, irá imprimir o valor de y no console.
    y = y + 1; // cada vez que o loop acontecer sera somanda mais 1 ao valor de y (0).
}
